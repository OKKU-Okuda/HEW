/*=====================================================================
プレイ矢―表示カメラ更新関数
=====================================================================*/
void ShowResultPlayerCam(CAMERA* pCam)
{
	const float CAMXLINE = -80.f;

	Vec3 StartPos(CAMXLINE, 30, 0), EndPos(CAMXLINE, 30, -150);
	float powrate;


	powrate = GetResultRate() * GetResultRate();
	pCam->pos = StartPos + (EndPos-StartPos) * powrate;

	pCam->at = Vec3(CAMXLINE, 0, 1000);
	pCam->up = Vec3(0, 1, 0);
}

