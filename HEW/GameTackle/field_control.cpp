/*=====================================================================
割合指定ランダム取得関数
戻り値：bool			true:割合でtrueにする
引数：　float rate			:0~1f　　trueを返す割合を指定
=====================================================================*/
bool CheckRand(float rate)
{
	DWORD value = rand();

	DWORD valmax = MAXDWORD * rate;
	
	if (value <= valmax)
	{// 許容範囲内であればtrue
		return true;
	}
	return false;
}